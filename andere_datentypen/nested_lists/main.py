# Creating an inventory
inventory = ["carrots", "bananas", "apples"]

# Adding a new item
inventory.append("oranges")

# Removing an outdated item
inventory.remove("bananas")

# Sorting the inventory
inventory.sort()

# Checking the result
print("Updated inventory:", inventory)

